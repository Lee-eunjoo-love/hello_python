#. 딕셔너리: 각 원소를 키와 값의 쌍으로 표현하고 쉼표로 구분한 후 중괄호로 감싸며 키는 원소를 구분하는 기준이므로 한 딕셔너리 내에서 중복 불가.
price = {"짜장면": 6000, "짬뽕": 7000, "탕수육": 20000}
print(price["짜장면"]) #. 6000
price["짬뽕밥"] = 7500
print(price["짬뽕밥"]) #. 원소 추가
del(price["짜장면"]) #. 원소 삭제
price["탕수육"] = 25000 #. 원소 값 수정
print(price)

fruit = {"사과": 2000, "포도": 3500, "오렌지": 4500}
