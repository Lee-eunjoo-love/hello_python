#. 현재 환율 정보
exchange_rates = {"USD": 1200, "EUR": 1400, "JYP": 11}
print("---------- 환율 정보 추가/수정 ----------")
key = input("추가하거나 수정할 통화 입력")
value = int(input(f"{key}의 현재 환율 금액을 입력하세요:"))
exchange_rates[key] = value
print(exchange_rates.items())
print("------------- 환율 계산기 -------------")
currency = input(f"통화({list(exchange_rates.keys())})를 입력하세요")
price = int(input(f"변환할 금액({currency})을 입력하세요"))
print(f"환율 계산 결과: {exchange_rates[key] * price}")